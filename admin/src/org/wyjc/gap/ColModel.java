package org.wyjc.gap;

public class ColModel {
private String name;
private String index;
private int width;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getIndex() {
	return index;
}
public void setIndex(String index) {
	this.index = index;
}
public int getWidth() {
	return width;
}
public void setWidth(int width) {
	this.width = width;
}

}
